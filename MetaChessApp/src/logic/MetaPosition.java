package logic;

public class MetaPosition {
	private int[] I;
	private int[] J;
	public int[] getI() {
		return I;
	}
	public void setI(int[] i) {
		I = i;
	}
	public int[] getJ() {
		return J;
	}
	public void setJ(int[] j) {
		J = j;
	}
	public MetaPosition(int[] i, int[] j) {
		I = i;
		J = j;
	}
	
}
